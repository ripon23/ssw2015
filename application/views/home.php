<h4>GramCar Expedition 2013-14</h4>       
<p>GramCar is a joint research initiative by Kyushu University (Japan), Toyota Motor Corporation (Japan) and Grameen Communications. The program endeavors to serve the villagers with transportation dependent facilities and explore a sustainable, as well as, affordable business model for such services. This year there are two 'Service Cars' operating at two of our research sites - Bheramara of Kushtia and Kalihati of Tangail. The program has commenced on September, 2013 and will continue through the end of February, 2014.</p>

<p>GramCar is serving people with six different services at five specified service points throughout Bheramara of Kushtia and Kalihati of Tangail on weekdays. It has already served more than 2,000 people at two project sites with health checkup and info-medicine service through GramHealth, blood group testing and registration as blood donor in GramBlood DataBank, transportation service for college girls, social product delivery, emergency transportation service, internet browsing, video calling, job search through GramJobs and many other information services.</p>


<h4>SSW Program 2012</h4>
<p>"Social Services on Wheels" is an Initiative to Reach the Unreached ones and is the result of a joint-research program between GramWeb of Global Communication Center under Grameen Communications and Toyota Company, Japan. The project endeavors to replace the infrastructural limitations towards economic and social development by offering ICT, healthcare, entrepreneurship and skill development services through the Nomadic Internet, Information and e-Health Kiosk.</p>

<p>Creating awareness about ICT based social services in unreached community, developing entrepreneurship and identifying new business opportunities are some the major goals of the project. </p>

